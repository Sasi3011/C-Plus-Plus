#include<iostream>
using namespace std;
int main()
{
    int a,b;
    cin>>a>>b;
    cout<<"a+b"<<a+b<<endl;
    cout<<"a-b"<<a-b<<endl;
    cout<<"a*b"<<a*b<<endl;
    cout<<"a/b"<<a/b<<endl;
    cout<<"Post increment = a++"<<a++<<endl;
    cout<<"Pre increment = ++a"<<++a<<endl;
    cout<<"Post decrement = b--"<<b--<<endl;
    cout<<"Pre decrement = --b"<<--b<<endl;
    return 0;
}